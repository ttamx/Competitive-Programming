void runcase(){
    int k;
    cin >> k;
    int row=0,col=0;
    vector<pii> ans;
    for(int i=500;i>=2;i--){
        int w=i*(i-1)/2;
        while(k>=w){
            k-=w;
            for(int j=0;j<i;j++)ans.emplace_back(row,col++);
            row++;
        }
    }
    assert(k==0);
    assert(ans.size()<=500);
    cout << ans.size() << "\n";
    for(auto [x,y]:ans)cout << x << " " << y << "\n";
}

