void runcase(){
    int n;
    cin >> n;
    vl a(n);
    for(auto &x:a)cin >> x;
    ll ans=0,mx=-1,sum=0,pre=0;
    for(auto x:a){
        sum+=x;
        if(mx!=-1)ans=min(ans,sum-mx);
        mx=max(mx,pre);
        pre=sum;
    }
    cout << accumulate(all(a),0LL)-2*ans << "\n";
}

